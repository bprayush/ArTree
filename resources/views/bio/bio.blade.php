                <p class="bioText">
                        Hit Man Gurung is a multimedia artist based in Kathmandu. His body of work is a direct response to some of the most pressing socio-political issues of the country. Most of Hit Man’s artwork addresses mass migration and haphazard urban development, aspects that are consequences of the ten year Maoist civil war and a reflection of an unstable, apathetic government. Deeply concerned about the impact of these larger forces on individuals, communities and societies. Hit Man infuses his paintings, documentary photo collage and installation work with political conviction and personal poetry.

                        <br><br>His recent series of work deals with Nepali migrant laborers who leave their families and country to join transitory work forces in foreign countries. International migration has increased exponentially since the civil war, causing disconnections between generations and altering structures of towns and villages. This trend has had a direct impact on traditional knowledge chains and local economies. These series highlight both the ensuing emotional landscape and the altered socio-economic realities.
                        
                        <br><br>Hit Man Gurung was born in 1986 in Nepal’s Lamjung district. He completed his M.F.A. from Tribhuwan University’s Central Department of Fine Arts in 2012. Recently, Hit Man Gurung was part of “Bearing Point” curated by Diana Betancourt Campbell at Dhaka Art Summit 2018. In 2017 Gurung was invited by Para Site Art Center, Hong Kong for International Artist Residency . Same year he was part of Dissensus - Group Art Exhibition, presented by Latitude 28 , Delhi, India. Gurung was selected for the 1st Kathmandu Triennale 2017. He was selected for the first Yinchuan Biennial 2016, hosted by the Yinchuan Museum of Contemporary Art (MOCA, Yinchuan) in China. He was also the featured artist at Moesgaard Museum of Denmark in 2016. In addition, his work was exhibited as part of “Afterwork” at Para Site Art Center, Hong Kong. Gurung was also selected for the 8th Asia Pacific Triennial of Contemporary Art (APT8) in Brisbane, Australia. He was represented by Nepal Art Council, Nepal and Blueprint12 Gallery, India for India Art Fair, 2016. Most recently, his work was exhibited as part of “Mining Warm Data” curated by Diana Campbell Betancourt at Dhaka Art Summit, 2016. In 2014. Gurung was nominated for the Signature Art Prize organized by the Asia Pacific Breweries Foundation in Singapore. That same year, he participated in the second Dhaka Art Summit, represented by Siddhartha Art Gallery. In 2013, Gurung was invited by The Center for Culture Development, Denmark (CKU) at Images Festival. That year, he was also selected for an International Artists Residency granted by the city of Los Angeles, Department of Cultural Affairs. He was one of the selected artists for the second Kathmandu International Arts Festival which was held in 2012. Gurung is a co founder of the artist collective, Artree Nepal.
